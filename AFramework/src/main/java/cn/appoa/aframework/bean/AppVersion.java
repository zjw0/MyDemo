package cn.appoa.aframework.bean;

import java.io.Serializable;

public class AppVersion implements Serializable {

    public String AndroidTitle;//标题
    public String AndroidContents;//内容
    public int AndroidVersion;//版本号
    public String AndroidVersionName;//版本名
    public boolean AndroidIsUpdate;//是否强制更新
    public String AndroidFilePath;//下载地址
    public String AndroidUpdTime;//更新时间

    public AppVersion() {
    }

    public AppVersion(String AndroidTitle, String AndroidContents,
                      int AndroidVersion, String AndroidVersionName,
                      boolean AndroidIsUpdate, String AndroidFilePath, String AndroidUpdTime) {
        this.AndroidTitle = AndroidTitle;
        this.AndroidContents = AndroidContents;
        this.AndroidVersion = AndroidVersion;
        this.AndroidVersionName = AndroidVersionName;
        this.AndroidIsUpdate = AndroidIsUpdate;
        this.AndroidFilePath = AndroidFilePath;
        this.AndroidUpdTime = AndroidUpdTime;
    }
}
